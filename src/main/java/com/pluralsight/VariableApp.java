package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {

        // Step 1 --
        //Favorite color
        String favoriteColor;
        favoriteColor= "Green";
        System.out.println("My favorite Color "+favoriteColor);

        // Year-Up Start Year
        short year;
        year = 2026;
        System.out.println("I started Year up in " + year );

        //Middle Initial
        char middleInitial;
        middleInitial ='B';
        System.out.println("My middle initial is: " + middleInitial);

        //Has Pet
        boolean hasPet;
        hasPet = true;
        System.out.println("Do I have a pet "+ hasPet );

        //Nice Message
        String niceMessage;
        niceMessage = "Your doing a great job, keep up the good work";
        System.out.println(niceMessage);


        // Step 2--
        //Number of Days in week
        int numberOfDaysInweek;
        numberOfDaysInweek = 7;
        System.out.println("There are " + numberOfDaysInweek + " days in a week");

        //Price of coffee
        double coffeePrice;
        coffeePrice = 4.99;
        System.out.println("Coffee is " + coffeePrice);

        //Favorite Letter
        char favoriteLetter;
        favoriteLetter = 'S';
        System.out.println("My favorite letter: "+ favoriteLetter);

        //Is It Raining
        boolean isItRaining;
        isItRaining = true;
        System.out.println("is it raining: " + isItRaining);












    }
}
